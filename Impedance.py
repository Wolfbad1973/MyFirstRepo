import numpy as np

# Параметры цепи
R = 100  # Ом
L = 10e-3  # Гн
C = 1e-6  # Ф
f = 1000  # Гц

# Расчет реактивных сопротивлений
XL = 2 * np.pi * f * L
XC = 1 / (2 * np.pi * f * C)

# Импеданс
Z = R + 1j * (XL - XC)

# Модуль импеданса
Z_abs = np.abs(Z)

print(f"Импеданс: {Z} Ом")
print(f"Модуль импеданса: {Z_abs:.2f} Ом")