import math

# Параметры катушки
mu0 = 4 * math.pi * 1e-7  # Магнитная постоянная (Гн/м)
mu_r = 1  # Относительная магнитная проницаемость (воздух)
N = 9  # Количество витков
A = 1e-4  # Площадь поперечного сечения (м²)
l = 0.01  # Длина катушки (м)

# Расчет индуктивности
L = (mu0 * mu_r * N**2 * A) / l
print(f"Индуктивность: {L * 1e6:.2f} мкГн")
